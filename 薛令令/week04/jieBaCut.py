#词典；每个词后方存储的是其词频，词频仅为示例，不会用到，也可自行修改
Dict = {"经常":0.1,
        "经":0.05,
        "有":0.1,
        "常":0.001,
        "有意见":0.1,
        "歧":0.001,
        "意见":0.2,
        "分歧":0.2,
        "见":0.05,
        "意":0.05,
        "见分歧":0.05,
        "分":0.1}

#待切分文本
sentence = "经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(sentence, Dict):
    #TODO
    n = len(sentence)
    results = [[[]]]  # 初始化，从头开始记录切分方案

    # 循环切分
    for i in range(n):
        new_results = []  # 存储从当前位置的切分
        for path in results[-1]:  # 遍历上一轮的切分路径
            for j in range(i + 1, n + 1):  # 从当前字符向后切分
                word = sentence[i:j]
                if word in Dict:  # 如果找到合法的词
                    new_results.append(path + [word])  # 生成新的切分路径
        results.append(new_results)

    # 最后一个位置的切分路径就是结果
    return results[-1]

results = all_cut(sentence, Dict)

# 输出所有切分结果
for i, result in enumerate(results, 1):
    print(f"切分方案 {i}: {' | '.join(result)}")

